
// Object Literal Syntax

let circle = {
  // Below are properties is used to hold values
  radius: 1,
  location: {
    x: 1,
    y: 1,
  },
  // Below is function or a method is used to define login
  draw: function () {
    console.log('drawing circle');
  }
};

// calling function using dot notation
circle.draw();